				<h2>Explaination</h2>
				<div class="panel">
					<h4>Navigation Bar</h4>
					<p>Navigating content is one of the most important things to consider during the design phase of your website. How you organize your content is reflected by the menu and should be simple and intuitive. The NavBar is displayed on every page, and determines what is displayed in the main content area.</p>

					<h4>Header</h4>
					<p>The top-most section of your webite displayed above the content of every page is called the header. Headers can be used to create a memorable image, and stylize your brand. </p>

					<h4>Content Area</h4>
					<p>The central content areas of the website that holds the meat and potatoes. This is where you'll see galleries, product information, store locations, or anything else you want to feature online. A good layout will showcase your business by providing clear and thought-out presentation of your material, and continue to attract your clientelle to the many facets of your business.</p>

					<h4>Info Boxes</h4>
					<p>Often seen on the front page of your website, box layouts can be used to break up your content into sub-categories or key areas. Perfect for mission statements or tiered payment structures.</p>

					<h4>Side Bar</h4>
					<p>The main content area of your website can become full and cluttered if there is too much of the same thing in one place. Utilizing different tools and widgets, sidebars can provide additional control over what is being displayed. Some common sidebar features are menus, ad space, blog post archives, and social media links and affiliates.</p>

					<h4>Widgets</h4>
					<p>Widgets are tools and plugins that customize your page by increasing the number of ways your users interact with the content. Mailing lists, voting polls, suggestion boxes, and many other small functions act to increase the user experience by gathering information and data to provide personalized content for your clientelle.</p>

					<h4>Footer</h4>
					<p>At the bottom of your website, footers can be used for a number of different functions. When a user reaches the bottom of a page's content, a footer can serve the same navigational purpose as a header by containing links and menus. A website should always aim to keep users engaged, and the footer is a great way to guide people through your content.</p>

